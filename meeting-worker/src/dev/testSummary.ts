// Dev helper: прогоняет summarize() на синтетическом транскрипте и печатает
// результат — чтобы проверить качество промпта без реального созвона.
//
//   node --env-file=.env -r ts-node/register src/dev/testSummary.ts

import { summarize } from "../worker/summaryWriter";

// Синтетическая предзащита: 4 команды выступают по очереди, эксперты дают
// обратную связь в конце. Старый промпт ("3-5 предложений") схлопывал такое
// в один общий вывод про концовку; новый должен пройтись по каждой команде.
const TRANSCRIPT = `
Модератор: Коллеги, начинаем предзащиту. Первой выступает команда «Atom».
Виктор Бондаренко: Мы делаем Telegram-бота для автоматизации задач, встреч и коммуникаций. Реализовали интеграцию с YouGile, транскрибацию созвонов и анализ эмоционального состояния участников.
Эксперт Иванов: Технически зрелый проект. Замечание по презентации — слайды перегружены, поправьте шрифты.
Модератор: Спасибо. Следующая команда — «Орбита».
Елизавета Прокопенко: Наш проект — сервис подбора менторов для студентов. Сделали матчинг по интересам и расписанию, MVP уже работает.
Эксперт Петрова: Хорошая идея, но не хватает метрик. Добавьте к финалу цифры по удержанию пользователей.
Модератор: Дальше команда «Вектор».
Платон: Мы разрабатываем платформу для совместной работы над досками. Сейчас есть базовый редактор и синхронизация в реальном времени.
Эксперт Иванов: Сильная техническая часть. Нужно показать сценарий использования вживую на демо.
Модератор: И последняя команда — «Спектр».
Андрей: Наш продукт — система мониторинга качества воздуха с датчиками и дашбордом. Собрали прототип устройства и веб-панель.
Эксперт Петрова: Аппаратная часть впечатляет, но проработайте бизнес-модель — кто платит за датчики.
Модератор: Подводим итоги. Все команды допущены к финалу. Общие рекомендации: улучшить оформление презентаций, тщательно отрепетировать демонстрации с учётом тайминга.
`.trim();

async function main(): Promise<void> {
  console.log(`transcript length: ${TRANSCRIPT.length} chars\n`);
  const { summary, tasks } = await summarize(TRANSCRIPT, "Предзащита проектов");

  console.log("=== SUMMARY ===\n" + summary + "\n");
  console.log("=== TASKS (" + tasks.length + ") ===");
  for (const t of tasks) {
    console.log(`- ${t.title} | исполнитель: ${t.assignee || "—"} | дедлайн: ${t.deadline || "—"}`);
  }
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
